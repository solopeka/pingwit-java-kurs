package com.pingwit.lesson4.classwork;

public class Task1Main {

    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        int i = 0;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        System.out.println("======");

        int x = 15;
        do {
            System.out.println(x);
            x++;
        } while (x < 15);

        System.out.println("======");

        for (int j = 10; j >= 0; j--) {
            System.out.println(j);
        }

        System.out.println("======");

        int k = 10;
        while (k >= 0) {
            System.out.println(k);
            k--;
        }



    }
}
